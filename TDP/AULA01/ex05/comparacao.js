//maior que >
console.log("2 > 1" + (2 > 2))

//menor igual a >
console.log("2 >= 1" + (2 >= 2))

//menor que >
console.log("2 < 1" + (2 < 2))

//menor igual a <= 
console.log("2 <= 2" + (2 <= 2))

//igual a == (comparação)
console.log("2 == 2" + (2 == "2"))

//identico a ===
console.log("2 === 2" + (2 === "2"))

//diferente !=
console.log("2 != 2" + (2 != 3))